import { rm } from "node:fs/promises";

import { appendRuntimeTypeFacades, renderPrimitiveIndex } from "../../primitive-index.js";
import { createTsHeader, writeGeneratedFile } from "../../shared.js";
import type { FrameworkAdapterTargetPrimitivePackageGenerator } from "../types.js";

export function createVueComponentHeader(generatedBy: string): string {
  return `<!-- Generated by ${generatedBy}. Do not edit by hand. -->\n`;
}

export const generateVuePrimitivePackage: FrameworkAdapterTargetPrimitivePackageGenerator = async ({
  components,
  generatePrimitiveEntries,
  generatedBy,
  outputRoot,
}) => {
  const componentHeader = createVueComponentHeader(generatedBy);
  const moduleHeader = createTsHeader(generatedBy);

  await rm(outputRoot, { force: true, recursive: true });
  await Promise.all([
    generatePrimitiveEntries({ componentHeader, moduleHeader, outputRoot }),
    writeGeneratedFile(outputRoot, "index.ts", renderPrimitiveIndex(moduleHeader, components)),
  ]);
  await appendRuntimeTypeFacades(outputRoot, components);
};
